package com.example.myfirstapp.Logic;

public class FallingObject {

    private int posI;
    private int posJ;

    public FallingObject(int posI, int posJ) {
        this.posI = posI;
        this.posJ = posJ;
    }

    public int getposI() {
        return posI;
    }

    public int getposJ() {
        return posJ;
    }

    public void setposI(int posI) {
        this.posI = posI;
    }

    public void setposJ(int posJ) {
        this.posJ = posJ;
    }



}
